_REPO_ROOT = pathlib.Path(__file__).parents[2]
_SIDECAR_ROOT = _REPO_ROOT / "kernel" / "sidecar"
_FIXTURES_ADVERSARIAL = _REPO_ROOT / "fixtures" / "adversarial"

if str(_REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(_REPO_ROOT))
if str(_SIDECAR_ROOT) not in sys.path:
    sys.path.insert(0, str(_SIDECAR_ROOT))


def _import_guard():
    """Import injection_guard module."""
    mod_name = "kernel.sidecar.models.injection_guard"
    if mod_name in sys.modules:
        return sys.modules[mod_name]
    spec = importlib.util.spec_from_file_location(
        mod_name,
        _SIDECAR_ROOT / "models" / "injection_guard.py",
    )
    mod = importlib.util.module_from_spec(spec)
    sys.modules[mod_name] = mod  # register BEFORE exec so @dataclass can find module
    spec.loader.exec_module(mod)
    return mod


GUARD = _import_guard()


