class TestManifest:
    """Validate the injection fixture manifest exists and is complete."""

    def test_manifest_exists_after_generation(self):
        """After running generate_injection_pdfs.py, manifest must exist."""
        manifest_path = _FIXTURES_ADVERSARIAL / "injection_manifest.json"
        if not manifest_path.exists():
            # Try to generate
            gen_path = _FIXTURES_ADVERSARIAL / "generate_injection_pdfs.py"
            if gen_path.exists():
                result = subprocess.run(
                    [sys.executable, str(gen_path)],
                    cwd=str(_FIXTURES_ADVERSARIAL),
                    capture_output=True,
                    text=True,
                )
                assert result.returncode == 0, f"Generation failed: {result.stderr}"

        assert manifest_path.exists(), "injection_manifest.json must exist"

    def test_manifest_contains_all_fixtures(self):
        """Manifest must list all 10 fixtures (9 injection + 1 clean)."""
        manifest_path = _FIXTURES_ADVERSARIAL / "injection_manifest.json"
        if not manifest_path.exists():
            pytest.skip("Manifest not yet generated")

        with open(manifest_path) as fh:
            manifest = json.load(fh)

        assert len(manifest) == 10, (
            f"Expected 10 fixtures in manifest, got {len(manifest)}"
        )

        filenames = {f["filename"] for f in manifest}
        assert "clean_normal.pdf" in filenames, "Control fixture missing"
        injection_fixtures = [f for f in manifest if f["filename"] != "clean_normal.pdf"]
        assert len(injection_fixtures) == 9, "Expected 9 injection fixtures"

    def test_pdfs_exist(self):
        """All fixture PDFs referenced in manifest must exist on disk."""
        manifest_path = _FIXTURES_ADVERSARIAL / "injection_manifest.json"
        if not manifest_path.exists():
            pytest.skip("Manifest not yet generated")

        with open(manifest_path) as fh:
            manifest = json.load(fh)

        missing = []
        for entry in manifest:
            pdf_path = _FIXTURES_ADVERSARIAL / entry["filename"]
            if not pdf_path.exists():
                missing.append(entry["filename"])

        assert not missing, f"Missing PDFs: {missing}"
