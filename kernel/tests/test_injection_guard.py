class TestInjectionSummary:
    def test_summary_no_raw_text(self):
        """Injection summary must not contain raw document text — only metadata."""
        from kernel.sidecar.models.injection_guard import (
            InjectionMatch,
            InjectionScanResult,
            get_injection_summary,
        )
        results = [
            InjectionScanResult(
                chunk_id="c1",
                is_clean=False,
                matches=[InjectionMatch(
                    pattern="ignore.*previous.*instructions",
                    span_start=0,
                    span_end=35,
                )],
                action="quarantine",
            ),
            InjectionScanResult(
                chunk_id="c2",
                is_clean=True,
                matches=[],
                action="allow",
            ),
        ]
        summary = get_injection_summary(results)
        # Summary must not contain any raw matched text
        summary_str = json.dumps(summary)
        assert "ignore previous instructions" not in summary_str
        # Must have structural fields
        assert summary["total_chunks"] == 2
        assert summary["quarantined"] == 1
        assert summary["clean"] == 1
        assert "c1" in summary["quarantined_chunk_ids"]


